package HeapsAndPriorityQueue;

public class SlidingWindowMaximum {
    public static void main(String[] args) {
        int arr[] = {2, 4, 2, 5, 35, 22, 5};
        for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i]+" ");
        }
        System.out.println();

        int a = 10, b = 90;
        int c = a + b;
        System.out.println(c);
    }
}
